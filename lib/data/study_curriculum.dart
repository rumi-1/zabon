const String sampleAudio = 'assets/audio/sample_listen.mp3';

List<PathUnit> curriculumFor(StudyLanguage lang) {
  return lang == StudyLanguage.dari ? _dariUnits : _pashtoUnits;
}

List<String> allLessonIdsInOrder(StudyLanguage lang) {
  final units = curriculumFor(lang);
  return [
    for (final u in units) ...u.lessons.map((l) => l.id),
  ];
}

