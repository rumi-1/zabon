/// Flattened exercises for quick practice mode.
List<LessonExercise> allExercisesShuffled(StudyLanguage lang) {
  final out = <LessonExercise>[];
  for (final u in curriculumFor(lang)) {
    for (final l in u.lessons) {
      out.addAll(l.exercises);
    }
  }
  return out;
}
